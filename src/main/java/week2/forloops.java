package week2;

public class forloops
{
    public static void main(String[] args) {
        // print number 1-5
        for (int x=0; x <5; x++) {
            System.out.println(x+1);

        }
        // print 10,9,8 ....
        for (int x=10; x>=0; x--) {
            System.out.println(x + "...");
        }
        System.out.println("Rocket Launch!!!");

    }
}
